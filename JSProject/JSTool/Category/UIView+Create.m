@implementation UIView (Create)

/*
 frame  : frame
 cornerRadius : 半径
 borderColor : 边框的颜色
 borderWidth : 边框的宽度
 **/
+ (UIView *)createViewWithFrame:(CGRect)frame
                   cornerRadius:(CGFloat)cornerRadius
                    borderColor:(UIColor *)borderColor
                    borderWidth:(CGFloat)borderWidth
                backGroundColor:(UIColor *)backGroundColor {
    UIView *view = [[UIView alloc] initWithFrame:frame];
    if(cornerRadius) {
        view.layer.cornerRadius = cornerRadius;
        view.layer.borderColor = borderColor.CGColor;
        view.layer.borderWidth = borderWidth;
        view.layer.masksToBounds = YES;;
    }
    if(backGroundColor) {
        view.backgroundColor = backGroundColor;
    }
    return view;
}

/*
 descripetion : 创建view视图不切圆角
 frame : frame
 backGroundColor : 背景的颜色
 */
+ (UIView *)createViewWithFrame:(CGRect)frame
                backGroundColor:(UIColor *)backGroundColor {
    UIView *view = [UIView createViewWithFrame:frame cornerRadius:0 borderColor:[UIColor clearColor] borderWidth:0 backGroundColor:backGroundColor];
    return view;
}


- (void)clipWithCornerRadius:(CGFloat)cornerRadius borderColor:(id)borderColor borderWidth:(CGFloat)borderWidth{
    self.clipsToBounds = YES;
    self.layer.cornerRadius = cornerRadius;
    self.layer.borderColor = ((UIColor *)borderColor).CGColor;
    self.layer.borderWidth = borderWidth;
}

#pragma mark - 设置部分圆角
/**
 *  设置部分圆角(绝对布局)
 *
 *  @param corners 需要设置为圆角的角 UIRectCornerTopLeft | UIRectCornerTopRight | UIRectCornerBottomLeft | UIRectCornerBottomRight | UIRectCornerAllCorners
 *  @param radii   需要设置的圆角大小 例如 CGSizeMake(20.0f, 20.0f)
 */
- (void)addRoundedCorners:(UIRectCorner)corners
                withRadii:(CGSize)radii {
    
    UIBezierPath* rounded = [UIBezierPath bezierPathWithRoundedRect:self.bounds byRoundingCorners:corners cornerRadii:radii];
    CAShapeLayer* shape = [[CAShapeLayer alloc] init];
    [shape setPath:rounded.CGPath];
    
    self.layer.mask = shape;
}

/**
 *  设置部分圆角(相对布局)
 *
 *  @param corners 需要设置为圆角的角 UIRectCornerTopLeft | UIRectCornerTopRight | UIRectCornerBottomLeft | UIRectCornerBottomRight | UIRectCornerAllCorners
 *  @param radii   需要设置的圆角大小 例如 CGSizeMake(20.0f, 20.0f)
 *  @param rect    需要设置的圆角view的rect
 */
- (void)addRoundedCorners:(UIRectCorner)corners
                withRadii:(CGSize)radii
                 viewRect:(CGRect)rect {
    
    UIBezierPath* rounded = [UIBezierPath bezierPathWithRoundedRect:rect byRoundingCorners:corners cornerRadii:radii];
    CAShapeLayer* shape = [[CAShapeLayer alloc] init];
    [shape setPath:rounded.CGPath];
    
    self.layer.mask = shape;
    
}

